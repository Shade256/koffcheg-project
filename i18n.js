document.addEventListener('DOMContentLoaded', () => {
    let currentLang = 'ru';
    const langToggle = document.getElementById('lang-toggle');
    
    if (langToggle) {
        langToggle.addEventListener('click', () => {
            currentLang = currentLang === 'ru' ? 'en' : 'ru';
            langToggle.innerText = currentLang === 'ru' ? 'EN' : 'RU';
            
            elementsMap.forEach(item => {
                const el = document.querySelector(item.s);
                if (el) {
                    el.innerHTML = translations[currentLang][item.k];
                }
            });
            
            localStorage.setItem('kofcheg_lang', currentLang);
        });
        
        const savedLang = localStorage.getItem('kofcheg_lang');
        if (savedLang && savedLang === 'en') {
            langToggle.click();
        }
    }
});
